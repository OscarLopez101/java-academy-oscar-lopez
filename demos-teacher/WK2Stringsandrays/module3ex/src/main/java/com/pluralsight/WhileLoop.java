package com.pluralsight;

public class WhileLoop {

    public static void main(String[] args) {
        int i = 1;

        while (i <= 5) {
            System.out.println("I love Java");
            i++;
        }
    }
}

class DoWhileLoop {
    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println("I Love Java");
            i++;
        } while (i <= 5);


    }

    }
